package com.rapido.rider.things.watch;

public class WatchModifier {

	public Watch watch;

	public void useWatch(Watch watch) {
		watch.setBrand("NOISE");
		watch.setType("Smart watch");
		watch.setBatteryBackUpDays(15);
		watch.setWeight(150);
		watch.setSize(1.69);
		watch.setManufacturedBy("NOISE");
		watch.setManufacturedOn("OCT - 2022");
		watch.setMarketedBy("NOISE");
		watch.setHeadQuaters("Taiwan");
		watch.setWarrantyInYears(2);
		watch.setEmail("care@noise.com");

	}
}
